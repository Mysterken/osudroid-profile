export const ALL_COUNTRIES_CODE = 'all';

export function getCountryName(code: string, locale = 'en'): string {
	try {
		return new Intl.DisplayNames([locale], { type: 'region' }).of(code) || 'Unknown Country';
	} catch {
		return 'Unknown Country';
	}
}

/**
 * Returns the list of countries available in the leaderboard filter,
 * sorted alphabetically by display name, with a leading "All" entry.
 */
export function getLeaderboardCountries(locale = 'en'): Country[] {
	const named = COUNTRY_CODES.map((code) => ({ code, name: getCountryName(code, locale) }));
	named.sort((a, b) => a.name.localeCompare(b.name, locale));
	return [{ code: ALL_COUNTRIES_CODE, name: 'All' }, ...named];
}
